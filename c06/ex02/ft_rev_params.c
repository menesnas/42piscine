#include <unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	j;

	i = 0;
	j = ac - 1;
	if (ac > 1)
	{
		while (j > 0)
		{
			i = 0;
			while (av[j][i] != '\0')
			{
				write(1, &av[j][i], 1);
				i++;
			}
			j--;
			ac--;
			write(1, "\n", 1);
		}
	}
	return (0);
}
