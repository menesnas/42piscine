#include <unistd.h>

void	ft_ultimate_div_mod(int *a, int *b)
{
	int	div;
	int	mod;

	if (*b != 0)
	{
		div = *a;
		mod = *b;
		*a = div / mod;
		*b = div % mod;
	}
}
